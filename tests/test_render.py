def test_cron_minute_in_0_to_59_range():
    for repo in (
        "example-org/a",
        "example-org/b",
        "example-org/example-docs",
        "example-org/example-app",
    ):
        assert 0 <= cron_minute_for_repo(repo) <= 59


def test_cron_minute_distributes_across_callers():
    repos = [f"example-org/r{i}" for i in range(20)]
    minutes = {cron_minute_for_repo(r) for r in repos}
    # Expect > half unique; not a strict guarantee but catches degenerate hashing.
    assert len(minutes) > 10



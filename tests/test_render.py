def test_render_python_app_v3_retry_setup_is_noop():
    """v3 template no longer references retry_setup — both flag values render identically.

    Guards against accidental re-wiring of the flag in a future change.
    """
    caller_retry_true = Caller(repo="example-org/example-app", tier="python-app", pilot=True, retry_setup=True)
    caller_retry_false = Caller(repo="example-org/example-app", tier="python-app", pilot=True, retry_setup=False)
    kwargs = dict(
        template_path=TEMPLATES / "python-app" / "ci.yml",
        templates_root=TEMPLATES,
        version="v3.0.3",
        synced="2026-04-24T12:00:00Z",
        cron_minute=None,
    )
    assert render_template(caller_entry=caller_retry_true, **kwargs) == render_template(
        caller_entry=caller_retry_false, **kwargs
    )



def test_render_template_passes_caller_entry_through_to_includes(tmp_path):
    tpl = tmp_path / "ci.yml"
    tpl.write_text("name: t\nsteps:\n  - run: echo pre\n  {{include: snippets/hello.yml}}\n")
    (tmp_path / "snippets").mkdir()
    (tmp_path / "snippets" / "hello.yml").write_text("- name: Hello\n  run: echo hi\n")

    got = render_template(
        template_path=tpl,
        templates_root=tmp_path,
        caller_entry=_caller_default(),
        version="v2",
        synced="2026-04-24T00:00:00Z",
        cron_minute=None,
    )
    assert "- name: Hello" in got
    assert "{{include:" not in got



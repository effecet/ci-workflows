@pytest.mark.skipif(shutil.which("actionlint") is None, reason="actionlint not installed")
@pytest.mark.parametrize(
    ("template_name", "cron_minute"),
    [
        ("gitleaks-sweep.yml", 17),
        ("ci.yml", None),
    ],
)
def test_rendered_output_passes_actionlint(tmp_path, template_name, cron_minute):
    got = render_template(
        template_path=TEMPLATES / "python-app" / template_name,
        templates_root=TEMPLATES,
        caller_entry=_caller_default(),
        version="v3.0.3",
        synced="2026-04-20T14:30:00Z",
        cron_minute=cron_minute,
    )
    out = tmp_path / "rendered.yml"
    out.write_text(got)
    result = subprocess.run(
        [
            "actionlint",
            "-config-file",
            str(REPO_ROOT / ".github" / "actionlint.yaml"),
            str(out),
        ],
        capture_output=True,
        text=True,
        check=False,
    )
    assert result.returncode == 0, result.stdout + result.stderr


